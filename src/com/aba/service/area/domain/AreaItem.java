package com.aba.service.area.domain;

public class AreaItem {

	private String id = "" ;
	private String areaid = "" ;
	private String cname = "" ;
	private String alias = "" ;
	private String areaidp = "" ;
	private String cnamep = "" ;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getAreaid() {
		return areaid;
	}
	public void setAreaid(String areaid) {
		this.areaid = areaid;
	}
	public String getCname() {
		return cname;
	}
	public void setCname(String cname) {
		this.cname = cname;
	}
	public String getAlias() {
		return alias;
	}
	public void setAlias(String alias) {
		this.alias = alias;
	}
	public String getAreaidp() {
		return areaidp;
	}
	public void setAreaidp(String areaidp) {
		this.areaidp = areaidp;
	}
	public String getCnamep() {
		return cnamep;
	}
	public void setCnamep(String cnamep) {
		this.cnamep = cnamep;
	}
	
}
